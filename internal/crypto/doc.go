// Package crypto реализует примитивы шифрования, используемые клиентом GophKeeper.
//
// Все секреты шифруются на клиенте до отправки на сервер (zero-knowledge модель).
// Сервер никогда не видит открытый текст.
//
// Шифрование: AES-256-GCM ([Encrypt], [Decrypt]).
// Формат шифротекста: nonce (12 байт) || ciphertext || GCM-тег (16 байт).
// Дополнительные данные (aad) опциональны; передавать nil для совместимости.
//
// Деривация ключа: Argon2id ([DeriveKey]) с параметрами time=1, memory=64 МиБ, threads=4.
// Вывод — 32 байта, пригодные для AES-256.
// Salt генерируется однократно при регистрации ([GenerateSalt]) и хранится на сервере.
package crypto
