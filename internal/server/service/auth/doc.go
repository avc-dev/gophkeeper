// Package auth реализует сервис аутентификации на стороне сервера GophKeeper.
//
// Пароли хешируются bcrypt с cost=12.
// JWT подписываются алгоритмом EdDSA (Ed25519), срок действия токена — 72 часа.
// KDF-salt генерируется сервером при регистрации и возвращается клиенту при входе;
// клиент использует его для деривации мастер-ключа (Argon2id) — сервер его не знает.
//
// Методы сервиса ([Service.Register], [Service.Login], [Service.ValidateToken])
// доступны через интерфейс, используемый в AuthInterceptor и обработчиках.
