// Package secret реализует клиентский сервис управления секретами.
//
// Обеспечивает zero-knowledge модель: все секреты шифруются AES-256-GCM
// мастер-ключом на клиенте до записи в локальный кеш и отправки на сервер.
// Сервер хранит только зашифрованные payload-ы.
//
// Поддерживаемые типы секретов: credential, card, text, binary.
// При недоступности сервера данные сохраняются локально со статусом pending
// и синхронизируются при следующем вызове [Service.PushPending].
